    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmply(txtMaBanh, sb,"Mã bánh cần phải nhập");
        DataValidator.validateEmply(txtTenBanh, sb,"Tên Bánh cần phải nhập");
        DataValidator.validateEmply(txtSoLuong, sb,"Số lượng Bánh cần phải nhập");
        DataValidator.validateEmply(txtGiaTien, sb,"Giá Tiền Bánh cần phải nhập");
        DataValidator.validateEmply(txtNgayNhap, sb,"Ngày Nhập Bánh cần phải nhập");
        DataValidator.validateEmply(txtMaNVNhap, sb,"Mã nhân viên nhập Bánh cần phải nhập");
        
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm,sb.toString(),"Lỗi");
            return;
        }
        try{
            Banh banh = new Banh();
            banh.setMaBanh(txtMaBanh.getText());
            banh.setTenBanh(txtTenBanh.getText());
            banh.setSizeBanh(rdoSizeS.isSelected()? "S" : rdoSizeM.isSelected()? "M": "L");
            banh.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
            banh.setGiaTien(Float.parseFloat(txtGiaTien.getText()));
            banh.setNgayNhap(txtNgayNhap.getText());
            banh.setMaNhanVien(txtMaNVNhap.getText());
              BanhDao dao = new BanhDao();
              if(dao.insert(banh)){
                  MessageDialogHelper.showMessageDialog(parentForm,"Thông tin bánh đã được lưu","Thông báo");
                  loadDaTaToTable();
              } else {
                  MessageDialogHelper.showMessageDialog(parentForm,"thông tin bánh không lưu được", "Thông báo");
              }
        } catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
        }    
    }//GEN-LAST:event_btnThemActionPerformed


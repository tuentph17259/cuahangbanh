     private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"Mã Bánh"," Tên Bánh","Size Bánh","Số lượng","Giá tiền","Ngày Nhập","Mã NV Nhập"});
        tblBanh.setModel(tblModel);
    }
      private void loadDaTaToTable(){
        try{
            BanhDao dao = new BanhDao();
            List<Banh> list = dao.finadAll();
            tblModel.setRowCount(0);
            for (Banh x : list) {
                tblModel.addRow(new Object[]{
                    x.getMaBanh(),x.getTenBanh(),x.getSizeBanh().equals("S")? "S" : x.getSizeBanh().equals("M")? "M":"L",
                    x.getSoLuong(),x.getGiaTien(),x.getNgayNhap(),x.getMaNhanVien()
                });
            }
            tblModel.fireTableDataChanged();
        } catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
        }
    }
   
